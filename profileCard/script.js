function adding(i, n, a, j) {
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    let container = document.createElement("div");
    let pic = document.createElement("div");
    let img = document.createElement("img");
    let name = document.createElement("div");
    let age = document.createElement("div");
    let job = document.createElement("div");
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    container.setAttribute("class", "container");
    pic.setAttribute("class", "pic");
    name.setAttribute("class", "name");
    age.setAttribute("class", "age");
    job.setAttribute("class", "job");
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    document.querySelector("form").insertAdjacentElement("afterend", container)
    container.appendChild(pic);
    pic.appendChild(img);
    pic.insertAdjacentElement("afterend", name);
    name.insertAdjacentElement("afterend", age);
    age.insertAdjacentElement("afterend", job);
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    img.setAttribute("src", i);
    name.innerHTML = n;
    age.innerHTML = `AGE:${a}`;
    job.innerHTML = j;
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}
let input = document.querySelectorAll(".a");
let submit = document.querySelector(".submit");
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
submit.addEventListener(("click"), (e) => {
    e.preventDefault();
    let isTrue = true;
    let i = 0;
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    while (isTrue == true && i < input.length) {
        if (input[i].value.trim() != "") {
            isTrue = true;
        }
        else {
            isTrue = false;
            break;
        }
        i++;
    }
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    if (!isTrue) {
        alert("fill every part")
    }
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    if (isTrue) {
        let img = input[0].value;
        let name = input[1].value;
        let age = input[2].value;
        let job = input[3].value;
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        adding(img, name, age, job);
        for (const i of input) {
            i.value = "";
        }
    }
}
)
